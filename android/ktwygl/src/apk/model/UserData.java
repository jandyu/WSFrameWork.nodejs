package apk.model;

public class UserData
{
	private long _uid;
	private String _phone_no;
	private String _name;
	private String _passwd;
	private String _nick_name;
	private String _head_pic;
	private String _device_id;
	private String _unit_id;
	private String _unit_title;
	
	
	public long get_uid()
	{
		return _uid;
	}
	public void set_uid(long _uid)
	{
		this._uid = _uid;
	}
	public String get_phone_no()
	{
		return _phone_no;
	}
	public void set_phone_no(String _phone_no)
	{
		this._phone_no = _phone_no;
	}
	public String get_name()
	{
		return _name;
	}
	public void set_name(String _name)
	{
		this._name = _name;
	}
	public String get_passwd()
	{
		return _passwd;
	}
	public void set_passwd(String _passwd)
	{
		this._passwd = _passwd;
	}
	public String get_nick_name()
	{
		return _nick_name;
	}
	public void set_nick_name(String _nick_name)
	{
		this._nick_name = _nick_name;
	}
	public String get_head_pic()
	{
		return _head_pic;
	}
	public void set_head_pic(String _head_pic)
	{
		this._head_pic = _head_pic;
	}
	public String get_device_id()
	{
		return _device_id;
	}
	public void set_device_id(String _device_id)
	{
		this._device_id = _device_id;
	}
	public String get_unit_id()
	{
		return _unit_id;
	}
	public void set_unit_id(String _unit_id)
	{
		this._unit_id = _unit_id;
	}
	public String get_unit_title()
	{
		return _unit_title;
	}
	public void set_unit_title(String _unit_title)
	{
		this._unit_title = _unit_title;
	}
	
}
